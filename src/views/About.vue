<script setup lang="ts">
import { onMounted, ref, computed } from "vue";
import { initDDC } from "../assets/main";
import type { Team } from "@/dto/team";
import { api } from "@/api";
import { BASE_URL, roles } from "@/var";

const teams = ref<Team[]>([]);
const chairman = computed(() => {
  return teams.value.filter((team) => team.role === "chairman")[0] as
    | Team
    | undefined;
});

const directors = computed(() => {
  return teams.value
    .filter((team) => team.role === "director")
    .sort((a, b) => a.level - b.level) as Team[];
});

const deputy = computed(() => {
  return teams.value
    .filter((team) => team.role === "deputy")
    .sort((a, b) => a.level - b.level) as Team[];
});

const fetchTeams = async () => {
  const response = await api.get("/teams?populate=*");
  teams.value = response.data.data;
};

const initReveal = () => {
  const revealObserver = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          entry.target.classList.add("is-visible");
          revealObserver.unobserve(entry.target);
        }
      });
    },
    { threshold: 0 },
  );

  requestAnimationFrame(() => {
    document
      .querySelectorAll(".reveal")
      .forEach((el) => revealObserver.observe(el));
  });
};

onMounted(() => {
  fetchTeams();
  initDDC();
  initReveal();
});
</script>
